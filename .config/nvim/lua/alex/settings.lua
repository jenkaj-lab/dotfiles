-- Settings
vim.opt.termguicolors = true	    -- Enable terminal colours
vim.o.number = true            	    -- Enable absolute line numbers
vim.o.relativenumber = true    	    -- Enable relative line numbers
vim.o.cursorline = true        	    -- Highlight the current line
vim.opt.wrap = false                -- Turn off text wrapping
--vim.cmd.colorscheme("catppuccin")   -- Use listed colour scheme
vim.opt.clipboard = "unnamedplus"   -- Use system clipboard

-- Tab Settings
vim.opt.tabstop = 4		            -- Spaces a tab counts for
vim.opt.shiftwidth = 4		        -- Spaces for each indent level
vim.opt.expandtab = true	        -- Use spaces instead of tabs
vim.opt.softtabstop = 4		        -- Spaces a 'tab' key will insert
